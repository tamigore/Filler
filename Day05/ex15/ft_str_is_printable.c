int		ft_str_is_printable(char *str)
{
	int i;
	int l;

	i = 0;
	l = 0;
	if (str[i] == 0)
		return (1);
	while (str[l])
		l++;
	while (str[i] >= 32 && str[i] <= 126)
	{
		if (str[i] == str[l - 1])
			return (1);
		i++;
	}
	return (0);
}
