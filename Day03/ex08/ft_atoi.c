int		ft_atoi(char *str)
{
	int nbr;
	int neg;

	nbr = 0;
	neg = 1;
	while ((str[0] >= '\t' && str[0] <= '\r') || str[0] == ' ')
	{
		str++;
	}
	if (str[0] == '-')
	{
		neg = -1;
		str++;
	}
	else if (str[0] == '+')
		str++;
	while ((str[0] != '\0') && str[0] >= '0' && str[0] <= '9')
	{
		nbr = nbr * 10;
		nbr = nbr + str[0] - '0';
		str++;
	}
	return (nbr * neg);
}
